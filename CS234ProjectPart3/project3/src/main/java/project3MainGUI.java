	private void btnModCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModCActionPerformed
		int maccountID = Integer.parseInt(txtMCAccID.getText());
		String mcname = txtMCName.getText();
		String mcphoneNumber = txtMCPhone.getText();
		double mcpoints = Double.parseDouble(txtMCPoints.getText());
		boolean mCusFound = false;

		for (Iterator<customer> iterator = customers.iterator(); iterator.hasNext();){ //start of the iterator
			customer mcust = iterator.next();
			if (mcust.accountID == maccountID){
				int i = customers.indexOf(mcust);
				customers.set(i, new customer(maccountID, mcname, mcphoneNumber, mcpoints));
				JOptionPane.showMessageDialog(this, "Customer Modified.");
				mCusFound = true;
			} //end of iterator
		}  

		for (Iterator<silverCustomer> iterator = silverCustomers.iterator(); iterator.hasNext();){ //start of the iterator
			silverCustomer mscust = iterator.next();
			if (mscust.accountID == maccountID){
				int i = silverCustomers.indexOf(mscust);
				silverCustomers.set(i, new silverCustomer(maccountID, mcname, mcphoneNumber, mcpoints, mscust.coupon));
				JOptionPane.showMessageDialog(this, "Customer Modified.");
				mCusFound = true;
			} //end of iterator
		}
		
		if (mCusFound = false){
			JOptionPane.showMessageDialog(this, "Customer Not Found.");
		}

		try {
			FileOutputStream fos = new FileOutputStream("customerData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(customers);
			oos.close();
			fos.close();
		} 

		catch (IOException ioe){
			ioe.printStackTrace();
		}

		try {
			FileOutputStream fos = new FileOutputStream("silcustomerData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(silverCustomers);
			oos.close();
			fos.close();
		} 

		catch (IOException ioe){
			ioe.printStackTrace();
		}

		txtMCAccID.setText("");
		txtMCName.setText("");
		txtMCPhone.setText("");
		txtMCPoints.setText("");
	}//GEN-LAST:event_btnModCActionPerformed

