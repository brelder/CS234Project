	private void btnMakePurchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMakePurchActionPerformed
		int addpaccountID = Integer.parseInt(txtPAccount.getText());
		int pinvoiceID = Integer.parseInt(txtPTran.getText());
		String pdate = txtPDate.getText();
		int consolePurchased = Integer.parseInt(txtPConsole.getText());
		int gamesPurchased = Integer.parseInt(txtPGames.getText());
		int controllersPurchased = Integer.parseInt(txtPCont.getText());
		int accessoriesPurchased = Integer.parseInt(txtPAcc.getText());
		int memoribiliaPurchased = Integer.parseInt(txtPMem.getText());

		double pamount = (consolePurchased * 100) + 
						(gamesPurchased * 50) + 
						(controllersPurchased * 20) + 
						(accessoriesPurchased * 10) + 
						(memoribiliaPurchased * 5);
							
					
		double ppointsEarned = pamount * .1;
		System.out.println("Points earned: " + ppointsEarned);

		txtPTotal.setText(String.format("%.2f", pamount));
		txtPPoints.setText(String.format("%.2f", ppointsEarned));
					
		for (Iterator<customer> iterator = customers.iterator(); iterator.hasNext();) { //add points to customer account
			customer addpcust = iterator.next();
			if (addpcust.accountID == addpaccountID) {
				addpcust.points = addpcust.points + ppointsEarned;
			}
		}

		for (Iterator<silverCustomer> iterator = silverCustomers.iterator(); iterator.hasNext();) { //add points to customer account
			customer addpscust = iterator.next();
			if (addpscust.accountID == addpaccountID) {
				addpscust.points = addpscust.points + ppointsEarned;
			}
		}

		//start of inventory block
		inventory.addConsole(consolePurchased); 
		inventory.addGames(gamesPurchased);
		inventory.addControllers(controllersPurchased);
		inventory.addAccessories(accessoriesPurchased);
		inventory.addMemoribilia(memoribiliaPurchased);
		//end of inventory block
				
		//start of invoice
		purchases.add(new purchaseInvoice (
							addpaccountID,
							pinvoiceID, 
							pdate, 
							consolePurchased, 
							gamesPurchased, 
							controllersPurchased, 
							accessoriesPurchased, 
							memoribiliaPurchased, 
							pamount, 
							ppointsEarned)); //invoice creation

		try {
			FileOutputStream fos = new FileOutputStream("purchaseData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(purchases);
			oos.close();
			fos.close();
		} 

		catch (IOException ioe){
			ioe.printStackTrace();
		}

		try {
			FileOutputStream fos = new FileOutputStream("customerData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(customers);
			oos.close();
			fos.close();
		} 
		
		catch (IOException ioe){
			ioe.printStackTrace();
		}
 
		try {
			FileOutputStream fos = new FileOutputStream("silcustomerData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(silverCustomers);
			oos.close();
			fos.close();
		} 

		catch (IOException ioe){
			ioe.printStackTrace();
		}
   
		try {
			FileOutputStream fos = new FileOutputStream("employeeData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(employees);
			oos.close();
			fos.close();
		} 

		catch (IOException ioe){
			ioe.printStackTrace();
		}

		try
        {
            String s = inventory.getConsole() + ",";
            s += inventory.getGames() + ",";
            s += inventory.getControllers() + ",";
            s += inventory.getAccesories() + ",";
			s += inventory.getMemoribilia();
            
            FileWriter w = new FileWriter(fileLocation);
            BufferedWriter writer = new BufferedWriter(w);
            writer.write(s);
            writer.close();
        }
		
        catch (IOException e)
        {
            JOptionPane.showMessageDialog(this, "File error");
        }
		
		JOptionPane.showMessageDialog(this, "Purchase Complete.");		

	}//GEN-LAST:event_btnMakePurchActionPerformed

