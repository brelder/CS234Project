	private void btnMakeSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMakeSaleActionPerformed
		int addsaccountID = Integer.parseInt(txtSAccount.getText());
		int addsempID = Integer.parseInt(txtSEmpID.getText());
		int sinvoiceID = Integer.parseInt(txtSTran.getText());
		String sdate = txtSDate.getText();
		int consoleSold = Integer.parseInt(txtSConsole.getText());
		int gamesSold = Integer.parseInt(txtSGames.getText());
		int controllersSold = Integer.parseInt(txtSCont.getText());
		int accessoriesSold = Integer.parseInt(txtSAcc.getText());
		int memoribiliaSold = Integer.parseInt(txtSMem.getText());

		double sAmount = (consoleSold * 100) + 
							(gamesSold * 50) + 
							(controllersSold * 20) + 
							(accessoriesSold* 10) + 
							(memoribiliaSold* 5);
	
		double spointsEarned = sAmount * .1;
		System.out.println("Points earned: " + spointsEarned);

		txtSTotal.setText(String.format("%.2f", sAmount));
		txtSPoints.setText(String.format("%.2f", spointsEarned));
					
		for (Iterator<customer> iterator = customers.iterator(); iterator.hasNext();) { //add points to customer account
			customer addscust = iterator.next();
			if (addscust.accountID == addsaccountID) {
				addscust.points = addscust.points + spointsEarned;
			}
		}

		for (Iterator<silverCustomer> iterator = silverCustomers.iterator(); iterator.hasNext();) { //add points to customer account
			customer addsscust = iterator.next();
			if (addsscust.accountID == addsaccountID) {
				addsscust.points = addsscust.points + spointsEarned;
			}
		}

		for (Iterator<employee> iterator = employees.iterator(); iterator.hasNext();) { //add amount to employee's sales total
			employee addsemp = iterator.next();
			if (addsemp.employeeID == addsempID) {
				addsemp.weeklySales = addsemp.weeklySales + sAmount;
			}
		}

		//start of inventory
		inventory.removeConsole(consoleSold);
		inventory.removeGames(gamesSold);
		inventory.removeControllers(controllersSold);
		inventory.removeAccessories(accessoriesSold);
		inventory.removeMemoribilia(memoribiliaSold);
		//end of inventory 
				
		//start of invoice
		sales.add(new salesInvoice (addsempID,
									addsaccountID,
									sinvoiceID, 
									sdate, 
									consoleSold, 
									gamesSold, 
									controllersSold, 
									accessoriesSold, 
									memoribiliaSold, 
									sAmount, 
									spointsEarned)); //invoice creation

		try {
			FileOutputStream fos = new FileOutputStream("saleData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(sales);
			oos.close();
			fos.close();
		} 

		catch (IOException ioe) {
			ioe.printStackTrace();
		}

		try {
			FileOutputStream fos = new FileOutputStream("customerData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(customers);
			oos.close();
			fos.close();
		} 
		
		catch (IOException ioe){
			ioe.printStackTrace();
		}

		try {
			FileOutputStream fos = new FileOutputStream("silcustomerData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(silverCustomers);
			oos.close();
			fos.close();
		} 

		catch (IOException ioe){
			ioe.printStackTrace();
		}

		try {
			FileOutputStream fos = new FileOutputStream("employeeData.dat");
			ObjectOutputStream oos = new ObjectOutputStream(fos);
			oos.writeObject(employees);
			oos.close();
			fos.close();
		} 

		catch (IOException ioe){
			ioe.printStackTrace();
		}

        try
        {
            String s = inventory.getConsole() + ",";
            s += inventory.getGames() + ",";
            s += inventory.getControllers() + ",";
            s += inventory.getAccesories() + ",";
			s += inventory.getMemoribilia();
            
            FileWriter w = new FileWriter(fileLocation);
            BufferedWriter writer = new BufferedWriter(w);
            writer.write(s);
            writer.close();
        }
		
        catch (IOException e)
        {
            JOptionPane.showMessageDialog(this, "File error");
        }
	
		JOptionPane.showMessageDialog(this, "Sale Complete.");
     
	}//GEN-LAST:event_btnMakeSaleActionPerformed

